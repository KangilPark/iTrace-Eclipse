package edu.ysu.itrace;

public class Aoi {
	private int x;
	private int y;
	private String name;
	
	public Aoi(int x, int y, String name) {
		this.x = x;
		this.y = y;
		this.name = name;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public String getName() {
		return name;
	}
}
